    public static int getDepthOfBestMoveTimed(Board board, MoveMasks moveMasks, int millis){
        long startTime = System.nanoTime(), finishTime = startTime + (millis * 1000000L);
        long nextDepthSearchTime = 0;
        int searchDepth = 0;

        Move[] moves = MoveGenerator.generateLegalMoves(board, moveMasks);

        while (System.nanoTime() + nextDepthSearchTime < finishTime) {
            searchDepth++;
            startTime = System.nanoTime();

            AlphaBeta.getBestMove(board, moves, searchDepth, moveMasks);

            nextDepthSearchTime = (System.nanoTime() - startTime) * 10;
            Arrays.sort(moves);
        }

        return searchDepth;
    }

    public static long getTimeOfDepth(Board board, MoveMasks moveMasks, int depth){
        long startTime = System.nanoTime();
        AlphaBeta.getBestMove(board, MoveGenerator.generateLegalMoves(board, moveMasks), depth, moveMasks);
        return System.nanoTime() - startTime;
    }

